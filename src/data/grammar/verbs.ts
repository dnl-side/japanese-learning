/** Returns n random verbs from the master list filtered by group */
export function getRandomVerbs(group: VerbGroup, n: number = 5): VerbEntry[] {
  const filtered = VERB_LIST.filter((v) => v.group === group);
  const shuffled = [...filtered].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
}

/** Returns all forms at a given JLPT level */
export function getFormsByLevel(level: JlptLevel): ConjForm[] {
  const order: JlptLevel[] = ["N5", "N4", "N3"];
  const idx = order.indexOf(level);
  return CONJUGATION_FORMS.filter((f) => order.indexOf(f.level) <= idx);
}