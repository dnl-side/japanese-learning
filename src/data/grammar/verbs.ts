import { RubySegment } from "@/app/components/ui/Ruby";

// ─── Types ────────────────────────────────────────────────────────────────────

export type VerbGroup = "godan" | "ichidan" | "irregular";
export type JlptLevel = "N5" | "N4" | "N3" | "N2"  |  "N1";
export type Tense = "presente" | "pasado" | "negativo" | "pasado-negativo";

/** A single verb in the master list */
export interface VerbEntry {
  dictionary: string;     // 買う
  kanji: string;          // 買
  furigana: string;       // か
  morpheme: string;       // う  (the part that changes)
  group: VerbGroup;
  meaning: string;        // comprar
  level: JlptLevel;
}

/** One conjugation example — maps to Flutter's kanjiRoot / furigana / morpheme structure */
export interface ConjExample {
  // Base verb
  kanji: string;          // 買
  furigana: string;       // か
  morpheme: string;       // う

  // Conjugated form
  conjKanji: string;      // 買
  conjFurigana: string;   // か
  conjMorpheme: string;   // います

  meaning: string;        // Compro (formal)

  // Optional contextual example sentence
  exampleSegments?: RubySegment[];
  exampleTranslation?: string;

  tense?: Tense;
}

/** Conjugation examples grouped by verb group */
export interface ConjGroup {
  group: VerbGroup;
  title: string;          // 五段活用 - Grupo 1
  rule: string;           // Brief rule explanation
  examples: ConjExample[];
}

/** A full conjugation form (ます, て, た, etc.) */
export interface ConjForm {
  key: string;            // "masu"
  title: string;          // "ます形"
  titleSegments: RubySegment[];
  level: JlptLevel;
  color: string;          // for the card accent
  description: string;
  usage: string;          // when/how to use it
  groups: ConjGroup[];
}

